package main

import "fmt"

func main() {

	/**** 🎃 variable and string-number 🎃 ****/

	// variable can declare with type and shorthand
	// string
	name := "aun"
	var name1 = "apunn"
	var name2 string = "pun"
	// number
	num := 1
	var num1 int = 2
	var num2 float32 = 1.3
	// bit and memory
	var value uint8 = 255
	var value1 uint16 = 30000
	fmt.Println(name, name1, name2, num, num1, num2, value, value1)
}
